inputFile = "src/Day16/short-input-0.txt"


soln :: IO ()
soln = 
  do bin_str <- parseInput <$> TIO.readFile inputFile
     putStrLn bin_str


parseInput :: T.Text -> String
parseInput = concatMap hexToBin . T.unpack

hexToBin :: Char -> String
hexToBin '0' = "0000"
hexToBin '1' = "0001"
hexToBin '2' = "0010"
hexToBin '3' = "0011"
hexToBin '4' = "0100"
hexToBin '5' = "0101"
hexToBin '6' = "0110"
hexToBin '7' = "0111"
hexToBin '8' = "1000"
hexToBin '9' = "1001"
hexToBin 'A' = "1010"
hexToBin 'B' = "1011"
hexToBin 'C' = "1100"
hexToBin 'D' = "1101"
hexToBin 'E' = "1110"
hexToBin 'F' = "1111"
hexToBin c = error $ "Invalid hex: " ++ [c]